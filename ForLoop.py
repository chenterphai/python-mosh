for item in 'Python':
    print(item)

print("---------------------------------------------")

# iteration loop over list of item
for item in ["Python", "Java", "C", "C++"]:
    print(item)

print("---------------------------------------------")

# number
for item in [1, 2, 3, 4]:
    print(item)

print("---------------------------------------------")

# range
for number in range(10):  # from 0 to 9
    print(number)

print("---------------------------------------------")

# from specify numbers
for number in range(2, 5):
    print(number)

print("---------------------------------------------")
# increment using range
for number in range(5, 10, 2):  # increase 2 in an iteration
    print(number)
